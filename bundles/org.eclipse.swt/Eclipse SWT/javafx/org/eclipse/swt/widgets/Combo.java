	@Override
	void createWidget() {
		ComboBox<String> control = new ComboBox<String>();
		control.setEditable((style & SWT.READ_ONLY) != SWT.READ_ONLY);
		items = FXCollections.observableArrayList();
		control.setItems(items);
		nativeControl = control;
	}
	
