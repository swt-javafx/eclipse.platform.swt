	@SuppressWarnings("unchecked")
	private ComboBox<String> getNativeCombo() {
		return (ComboBox<String>)nativeControl;
	}

	/**
	 * Returns a <code>Point</code> whose x coordinate is the character position
	 * representing the start of the selection in the receiver's text field, and
	 * whose y coordinate is the character position representing the end of the
	 * selection. An "empty" selection is indicated by the x and y coordinates
	 * having the same value.
	 * <p>
	 * Indexing is zero based. The range of a selection is from 0..N where N is
	 * the number of characters in the widget.
	 * </p>
	 * 
	 * @return a point representing the selection start and end
	 * 
	 * @exception SWTException
	 *                <ul>
	 *                <li>ERROR_WIDGET_DISPOSED - if the receiver has been
	 *                disposed</li>
	 *                <li>ERROR_THREAD_INVALID_ACCESS - if not called from the
	 *                thread that created the receiver</li>
	 *                </ul>
	 */
	public Point getSelection() {
		ComboBox<String> control = getNativeCombo();
		if( (style & SWT.READ_ONLY) == SWT.READ_ONLY ) {
			return new Point(0, control.getSelectionModel().getSelectedItem() == null ? 0 : control.getSelectionModel().getSelectedItem().length());
		} else {
			IndexRange range = control.getEditor().getSelection();
			return new Point(range.getStart(),range.getEnd());
		}
	}

